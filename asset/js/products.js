// Hàm render sản phẩm
function renderProducts(products, containerId) {
    const productSection = document.getElementById(containerId);

    if (!productSection) return;

    // Xóa nội dung cũ
    productSection.innerHTML = "";

    // Render sản phẩm mới
    products.forEach(product => {
        const productItem = document.createElement('a');
        productItem.className = 'product-item';
        productItem.href = product.link;

        productItem.innerHTML = `
            <img src="${product.img}" alt="${product.title}">
            <div class="product-title">${product.title}</div>
            <div class="product-price">${product.price}</div>
        `;

        productSection.appendChild(productItem);
    });
}

// Hàm xử lý sự kiện khi click vào collection item
function handleCollectionClick(event) {
    event.preventDefault(); // Ngăn hành động mặc định của thẻ <a>

    const category = event.currentTarget.getAttribute("data-category"); // Lấy danh mục
    const products = productData[category]; // Lấy sản phẩm theo danh mục

    if (products) {
        renderProducts(products, "bestseller-products"); // Render sản phẩm
    }
}

